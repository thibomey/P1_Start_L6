# Functie om twee getallen op te tellen
def tel_op(a, b):
    return a + b

# Functie om twee getallen van elkaar af te trekken
def trek_af(a, b):
    return a - b

# Functie om twee getallen met elkaar te vermenigvuldigen
def vermenigvuldig(a, b):
    return a * b

def delen(a, b):
    return a / b
